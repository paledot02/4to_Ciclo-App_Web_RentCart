	IDE_CAJ		INT		NOT NULL,
	NOM_CAJ		VARCHAR(30)	NOT NULL
)
GO

CREATE TABLE DISTRITO(
	IDE_DIS		INT		NOT NULL,
	DES_DIS		VARCHAR(40)	NOT NULL
