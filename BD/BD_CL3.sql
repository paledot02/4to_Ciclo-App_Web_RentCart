--Creando las tablas que componen la base de datos RENTCAR.
CREATE TABLE CLIENTE(
	IDE_CLI		INT		NOT NULL,
	APE_CLI 	VARCHAR(30)	NOT NULL,
	NOM_CLI		VARCHAR(30)	NOT NULL,
	DNI_CLI		CHAR(8)		NOT NULL,
