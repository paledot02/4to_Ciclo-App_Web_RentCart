	IDE_MAR		INT		NOT NULL,
	NOM_MAR		VARCHAR(30)	NOT NULL
)
GO

CREATE TABLE AUTOMOVIL(
	MAT_AUT		CHAR(10)		NOT NULL,
	COL_AUT		VARCHAR(30)		NOT NULL,
